# Arithmetic Operators
result_add <- 1 + 2
result_sub <- 5 - 3
result_mul <- 4 * 2
result_div <- 8 / 2

# Logical Operators
result_and <- TRUE & FALSE
result_or <- TRUE | FALSE

# Comparison Operators
result_eq <- (3 == 3)
result_neq <- (3 != 4)
result_lt <- (2 < 3)
result_gt <- (5 > 4)
result_le <- (3 <= 3)
result_ge <- (4 >= 3)

# Complex Expression
result_complex <- ifelse((1 + 2 == 3) & (4 > 2), 1, 0)

